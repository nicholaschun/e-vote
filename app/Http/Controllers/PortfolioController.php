     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(PortfolioRequest $request)
    {
        //Get portfolio name from form.

        $portfolio = Input::get('portfolio_name');
        if(empty($portfolio)){
            return redirect()->back()->withErrors('Portfolio name can not be empty');
        }
        $newPortfolio = new Portfolio();
        $newPortfolio->portfolio_name = $portfolio;
        $newPortfolio->save();
        return redirect()->back()->with('message','Portfolio Created Successfully');
    }

